public class Stations {
    String name;
    String line;
    String date;
    String depth;
    boolean hasConnection;

    public Stations(String name, String line, String date, String depth, boolean hasConnection) {
        this.name = name;
        this.line = line;
        this.date = date;
        this.depth = depth;
        this.hasConnection = hasConnection;
    }

}
